## Crie um programa que defina estas cinco funções: adicão, subtração, multiplicação, divisão e potência de a,b e imprima os resultados


def soma(a,b):
    return a + b

resultado = soma(5,5)
print("Sua soma é:",resultado)

def subtracao(a,b):
    return a - b

resultado = subtracao(8,3)
print("Sua subtração é:", resultado)

def multiplicacao(a,b):
    return a * b

resultado = multiplicacao(15,2)
print("Sua multiplicação é:",resultado)

def divisao(a,b):
    return a / b

resultado = divisao(90,5)
print("Sua divisão é:", resultado)

def potencia(a,b):
    return a ** b

resultado = potencia(4,2)
print("Sua potência é:", resultado)
